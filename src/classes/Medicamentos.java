    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
    model = (DefaultTableModel) jTable2.getModel();
    model.setRowCount(0);
        int media = 0;
    Medicamento med = new Medicamento();
     for (int i=0; i<Medicamento.vetmed.size(); i++) 
        media += Medicamento.vetmed.get(i).getQtd();
    
     media /= Medicamento.vetmed.size();
    txtMed.setText(String.valueOf(media));
    
    for (int i=0; i<Medicamento.vetmed.size(); i++){
        if((Medicamento.vetmed.get(i).getQtd()) < media){
            model.addRow(new Object[]{
                Medicamento.vetmed.get(i).getNome()
            });
        }
    }
    }//GEN-LAST:event_btnListarActionPerformed

