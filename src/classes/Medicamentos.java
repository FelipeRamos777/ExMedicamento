    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
    Medicamento med = new Medicamento();
    if (Medicamento.vetmed.isEmpty()){
    med.setCod(0);
    med.setNome(txtNome.getText());
    med.setVal(txtVal.getText());
    med.setQtd(Integer.parseInt(txtQtd.getText()));
    
    Medicamento.vetmed.add(med);
    }else{
    med.setCod(Medicamento.vetmed.size());
    med.setNome(txtNome.getText());
    med.setVal(txtVal.getText());
    med.setQtd(Integer.parseInt(txtQtd.getText()));
    
    Medicamento.vetmed.add(med);
    }
    JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso", "Atenção", 1);
    
    limpar();
    
    model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    
    for(int i=0; i<Medicamento.vetmed.size(); i++){
      model.addRow(new Object[]{
          Medicamento.vetmed.get(i).getCod(),
          Medicamento.vetmed.get(i).getNome(),
          Medicamento.vetmed.get(i).getVal(),
          Medicamento.vetmed.get(i).getQtd()
    });
    }
    }//GEN-LAST:event_btnGravarActionPerformed

